// Helper functions for working with recruiter data
export const getRecruiterById = (id) => {
  return recruiters.find(recruiter => recruiter.id === id);
};

export const getRecruitersByOffice = (office) => {
  return recruiters.filter(recruiter => recruiter.office === office);
};

export const getRecruitersByDepartment = (department) => {
  return recruiters.filter(recruiter => recruiter.department === department);
};

export const getRecruitersByStatus = (status) => {
  return recruiters.filter(recruiter => recruiter.status === status);
};

export const getTopPerformers = (limit = 5) => {
  return [...recruiters]
    .sort((a, b) => b.performanceMetrics.revenue - a.performanceMetrics.revenue)
    .slice(0, limit);
};

export const getRecruitersBySpecialization = (specialization) => {
  return recruiters.filter(recruiter => 
    recruiter.specializations.includes(specialization)
  );
};

export default recruiters;